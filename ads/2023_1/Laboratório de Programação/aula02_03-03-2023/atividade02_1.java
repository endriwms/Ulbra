// 1. Imprima todos os números inteiros de 10 a 25 utilizando uma estrutura de repetição.

public class atividade02_1 {
    public static void main(String[] args) {
        int numInt;

        for (numInt = 10; numInt <= 25; numInt++ ) {
            System.out.print(numInt + " ");
        }

    }
}
